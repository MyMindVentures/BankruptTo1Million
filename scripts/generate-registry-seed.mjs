#!/usr/bin/env node
import { readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import process from 'node:process';
import {
  collectRequiredKeys,
  entityContentToSql,
  extractExportFunctions,
  extractManifest,
  extractTranslationKeys,
  inferSurfaceType,
  loadConfig,
  manifestExportName,
  normalizePath,
  repoRoot,
  resolveSurfaceFiles,
  sqlString,
} from './i18n-script-utils.mjs';

const OUTPUT_PATH = path.join(
  repoRoot,
  'supabase/migrations/20260717161000_public_ui_component_registry_seed.sql',
);

function keySort(left, right) {
  return left.localeCompare(right);
}

function compareLinks(left, right) {
  const componentCompare = left.componentKey.localeCompare(right.componentKey);
  return componentCompare !== 0 ? componentCompare : left.translationKey.localeCompare(right.translationKey);
}

async function collectRegistryRows(config, files) {
  const components = [];
  const links = [];

  for (const absolutePath of files) {
    const relativePath = normalizePath(path.relative(repoRoot, absolutePath));
    const source = await readFile(absolutePath, 'utf8');
    const manifest = extractManifest(source);
    if (!manifest?.componentKey) continue;

    const publicExports = config.publicExportsOnly?.[relativePath];
    const scanSource = publicExports ? extractExportFunctions(source, publicExports) : source;
    const usedKeys = extractTranslationKeys(publicExports ? scanSource : source);
    const requiredKeys = collectRequiredKeys(manifest, usedKeys).sort(keySort);

    components.push({
      componentKey: manifest.componentKey,
      sourcePath: relativePath,
      exportName: manifestExportName(manifest.exportName),
      surfaceType: inferSurfaceType(relativePath, config),
      namespace: manifest.namespace || manifest.componentKey,
      entityContent: manifest.entityContent ?? null,
    });

    for (const translationKey of requiredKeys) {
      links.push({
        componentKey: manifest.componentKey,
        translationKey,
      });
    }
  }

  components.sort((left, right) => left.componentKey.localeCompare(right.componentKey));
  links.sort(compareLinks);

  return { components, links };
}

function buildComponentsBlock(components) {
  const rows = components.map((component) => (
    `  (${sqlString(component.componentKey)}, ${sqlString(component.sourcePath)}, ${sqlString(component.exportName)}, ${sqlString(component.surfaceType)}, ${sqlString(component.namespace)}, true, ${entityContentToSql(component.entityContent)}, 'connected')`
  ));

  return [
    'insert into public.website_ui_components',
    '  (component_key, source_path, export_name, surface_type, namespace, is_public, entity_content, coverage_status)',
    'values',
    `${rows.join(',\n')}`,
    'on conflict (component_key) do update set',
    '  source_path = excluded.source_path,',
    '  export_name = excluded.export_name,',
    '  surface_type = excluded.surface_type,',
    '  namespace = excluded.namespace,',
    '  is_public = excluded.is_public,',
    '  entity_content = excluded.entity_content,',
    '  coverage_status = excluded.coverage_status,',
    '  updated_at = now();',
    '',
  ].join('\n');
}

function buildLinksBlock(links) {
  const rows = links.map((link) => (
    `    (${sqlString(link.componentKey)}, ${sqlString(link.translationKey)})`
  ));

  return [
    'insert into public.website_ui_component_translation_keys (component_id, translation_key_id, usage_kind, is_required)',
    'select c.id, k.id, \'label\', true',
    'from public.website_ui_components c',
    'cross join lateral (',
    '  values',
    `${rows.join(',\n')}`,
    ') as v(component_key, translation_key)',
    'join public.website_translation_keys k on k.translation_key = v.translation_key',
    'where c.component_key = v.component_key',
    'on conflict (component_id, translation_key_id) do update set',
    '  usage_kind = excluded.usage_kind,',
    '  is_required = excluded.is_required,',
    '  updated_at = now();',
    '',
  ].join('\n');
}

async function main() {
  const config = await loadConfig();
  const files = await resolveSurfaceFiles(config);
  const { components, links } = await collectRegistryRows(config, files);

  const header = [
    '-- Generated by scripts/generate-registry-seed.mjs',
    `-- Seeds ${components.length} public UI components and ${links.length} translation key links.`,
    '',
    'begin;',
    '',
  ].join('\n');

  const body = [
    buildComponentsBlock(components),
    buildLinksBlock(links),
    'commit;',
    '',
  ].join('\n');

  await writeFile(OUTPUT_PATH, `${header}${body}`, 'utf8');
  console.log(`Wrote ${OUTPUT_PATH}`);
  console.log(`Components: ${components.length}; key links: ${links.length}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
